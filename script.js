var selectedWindow = undefined
// Step 1: Define a function called `dragElement` that makes an HTML element draggable.
function dragElement(element) {
  // Step 2: Set up variables to keep track of the element's position.
  var initialX = 0;
  var initialY = 0;
  var currentX = 0;
  var currentY = 0;

  // Step 3: Check if there is a special header element associated with the draggable element.
  if (document.getElementById(element.id + "header")) {
    // Step 4: If present, assign the `dragMouseDown` function to the header's `onmousedown` event.
    // This allows you to drag the window around by its header.
    document.getElementById(element.id + "header").onmousedown = startDragging;
  } else {
    // Step 5: If not present, assign the function directly to the draggable element's `onmousedown` event.
    // This allows you to drag the window by holding down anywhere on the window.
    element.onmousedown = startDragging;
  }

  // Step 6: Define the `startDragging` function to capture the initial mouse position and set up event listeners.
  function startDragging(e) {
    e = e || window.event;
    e.preventDefault();
    // Step 7: Get the mouse cursor position at startup.
    initialX = e.clientX;
    initialY = e.clientY;
    // Step 8: Set up event listeners for mouse movement (`elementDrag`) and mouse button release (`closeDragElement`).
    document.onmouseup = stopDragging;
    document.onmousemove = dragElement;
  }

  // Step 9: Define the `elementDrag` function to calculate the new position of the element based on mouse movement.
  function dragElement(e) {
    e = e || window.event;
    e.preventDefault();
    // Step 10: Calculate the new cursor position.
    currentX = initialX - e.clientX;
    currentY = initialY - e.clientY;
    initialX = e.clientX;
    initialY = e.clientY;
    // Step 11: Update the element's new position by modifying its `top` and `left` CSS properties.
    element.style.top = (element.offsetTop - currentY) + "px";
    element.style.left = (element.offsetLeft - currentX) + "px";
  }

  // Step 12: Define the `stopDragging` function to stop tracking mouse movement by removing the event listeners.
  function stopDragging() {
    document.onmouseup = null;
    document.onmousemove = null;
  }
}

// Basic window functionality
function closeWindow(closedWindow){
    document.getElementById(closedWindow).style.visibility = "hidden";
}
function openWindow(openedWindow){
    document.getElementById(openedWindow).style.visibility = "visible";
    document.getElementById(openedWindow).style.top = window.event.clientY + "px";
    document.getElementById(openedWindow).style.left = window.event.clientX-(window.innerWidth-window.innerHeight*(4/3))/2 + "px";
    selectWindow(document.getElementById(openedWindow))
    startWindowAnim(document.getElementById(openedWindow))
}
function loadWindows(){
    // Make the window elements draggable one by one.
    for(let i = 0; i < document.getElementsByClassName("window").length; i++){
        dragElement(document.getElementsByClassName("window")[i]);
        document.getElementsByClassName("window")[i].addEventListener("mousedown", () => selectWindow(document.getElementsByClassName("window")[i]));
    }
}

// Selecting/deselecting windows
var maxZ = 0;
function selectWindow(element){
    if(selectedWindow != undefined){
        selectedWindow.classList.remove("selected");
    }
    selectedWindow = element;
    selectedWindow.classList.add("selected");

    maxZ++;
    selectedWindow.style.zIndex = maxZ;
}
function deselectWindow(element){
    element.classList.remove("selected");
}

// Window animation
var lastUpdate = null
var delta = 0
var animatedWindow
var animationOffset = 8
var animationLength = 150.0
function startWindowAnim(element){
    if(options.animations){
        animatedWindow = element
        delta = 0
        requestAnimationFrame(windowAnim)
    }
}
function windowAnim(){
    if(delta < animationLength){
        if(lastUpdate == null){
            lastUpdate = Date.now()
        }
        var now = Date.now()
        delta += now - lastUpdate
        lastUpdate = now

        animatedWindow.style.transform = "translate(0, "+Math.round(animationOffset-(Math.sin(delta/animationLength*Math.PI/2))*animationOffset)+"px)"
        requestAnimationFrame(windowAnim)
    } else{
        lastUpdate = null
        animatedWindow.style.transform = "translate(0px, 0px)"
    }
}

// Basic file navigation
var currentFolder = []
function openFile(fileLocation){
    fileSearch = files
    console.log(fileLocation)
    for(let i = 0; i < fileLocation.length; i++){
        fileSearch = fileSearch.content[fileLocation[i]]
    }
    if(fileSearch.type == "image"){
        openImage(fileSearch)
    }
    if(fileSearch.type == "text"){
        openText(fileSearch)
    }
    if(fileSearch.type == "html"){
        openContent(fileSearch)
    }
    return fileSearch
}
function openRelativeFile(fileLocation){
    file = openFile(currentFolder.concat(fileLocation))
    if(file.type == "folder"){
        if(currentFolder[0] != 4 && currentFolder[1] != 0){currentFolder.push(fileLocation)}
        openFolder()
    }
}
function closeFolder(){
    currentFolder.pop()
    file = openFile(currentFolder)
    if(file.type == "folder"){
        openFolder()
    }
    return file
}
